package com.bear.entity;

public class SiteUser {
    
	private int id;
	private int siteId;
	public int getSiteId() {
		return siteId;
	}
	public void setSiteId(int siteId) {
		this.siteId = siteId;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	private String siteUserName;
	private String siteUserPwd;
	public String getSiteUserName() {
		return siteUserName;
	}
	public void setSiteUserName(String siteUserName) {
		this.siteUserName = siteUserName;
	}
	public String getSiteUserPwd() {
		return siteUserPwd;
	}
	public void setSiteUserPwd(String siteUserPwd) {
		this.siteUserPwd = siteUserPwd;
	}
	
}
